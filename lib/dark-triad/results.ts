export function getResultType(
  mNorm: number,
  nNorm: number,
  pNorm: number
): DarkTriadType {
  const mHigh = mNorm >= THRESHOLD;
  const nHigh = nNorm >= THRESHOLD;
  const pHigh = pNorm >= THRESHOLD;

  if (mHigh && nHigh && pHigh) return "dark-triad";
  if (mHigh && nHigh) return "charisma";
  if (mHigh && pHigh) return "dark-hero";
  if (nHigh && pHigh) return "psycho-king";
  if (mHigh) return "strategist";
  if (nHigh) return "king";
  if (pHigh) return "cold";
  return "saint";
}
